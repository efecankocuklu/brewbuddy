void SystemClock_Config_Custom(void)
{
  uint32_t tickstart;

  __HAL_RCC_HSI_CALIBRATIONVALUE_ADJUST(16);
  __HAL_RCC_HSI14ADC_DISABLE();
  __HAL_RCC_HSI14_ENABLE();

  tickstart = HAL_GetTick();
  while(__HAL_RCC_GET_FLAG(RCC_FLAG_HSI14RDY) == RESET)
  {
    if((HAL_GetTick() - tickstart) > HSI14_TIMEOUT_VALUE)
      break;
  }

  __HAL_RCC_HSI14_CALIBRATIONVALUE_ADJUST(16);
  MODIFY_REG(RCC->CFGR, RCC_CFGR_HPRE, RCC_SYSCLK_DIV8);

  __HAL_RCC_SYSCLK_CONFIG(RCC_SYSCLKSOURCE_HSI);

  /* Get Start Tick */
  tickstart = HAL_GetTick();

  while (__HAL_RCC_GET_SYSCLK_SOURCE() != RCC_SYSCLKSOURCE_STATUS_HSI)
  {
    if((HAL_GetTick() - tickstart ) > CLOCKSWITCH_TIMEOUT_VALUE)
      break;
  }

  MODIFY_REG(RCC->CFGR, RCC_CFGR_PPRE, RCC_HCLK_DIV1);
  SystemCoreClock = HAL_RCC_GetSysClockFreq() >> AHBPrescTable[(RCC->CFGR & RCC_CFGR_HPRE)>> RCC_CFGR_HPRE_BITNUMBER];

  HAL_InitTick (TICK_INT_PRIORITY);

  __HAL_RCC_I2C1_CONFIG(RCC_I2C1CLKSOURCE_HSI);

    /**Configure the Systick interrupt time
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

